curso = "  2   pyThOn   2    "

print(curso.upper())  # Converte todas as letras para maiúsculas.

print(curso.lower())  # Converte todas as letras para minúsculas.

print(curso.title())  # Converte para título, colocando a primeira letra de cada palavra em maiúscula.

print(curso.strip())  # Remove os espaços em branco no início e no final da string.

print(curso.lstrip("2"))  # Remove os caracteres '5' e '5' da esquerda da string.

print(curso.rsplit("2"))  # Divide a string em uma lista de substrings usando '44' como separador.

print(curso.center(10,'#'))  # Centraliza a string em um total de 10 caracteres, preenchendo com '#' nos espaços vazios.

print(".".join(curso))  # Une cada caractere da string com '.', formando uma nova string.